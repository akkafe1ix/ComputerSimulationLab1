from scipy.optimize import linprog

# Коэффициенты целевой функции
c = [5, 6, 7, 4]

# Коэффициенты ограничений-неравенств (преобразованные в ≤, т.к. linprog минимизирует)
A = [[-1, -1, 0, -4],  # -x1 - x2 - 4x4 ≤ -26
     [-2, 0, -3, -5],  # -2x1 - 3x3 - 5x4 ≤ -30
     [-1, -2, -4, -6]] # -x1 - 2x2 - 4x3 - 6x4 ≤ -24

# Правые части ограничений-неравенств
b = [-26, -30, -24]

# Решение задачи линейного программирования
res = linprog(c, A_ub=A, b_ub=b, bounds=(0, None))

# Вывод результата
print("Оптимальное значение:", res.fun)
print("Оптимальные значения переменных:", res.x)
